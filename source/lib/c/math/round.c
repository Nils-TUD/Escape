#include <esc/common.h>
#include <math.h>

double round(double x) {
	long pint = (long)x;
	double frac = x - pint;
	double absfrac = frac < 0 ? -frac : frac;
	return absfrac < 0.5 ? pint : (pint < 0 ? pint - 1 : pint + 1);
}
